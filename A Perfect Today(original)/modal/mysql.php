		  		MySQL follows the working of Client-Server Architecture. This model is designed for the end-users called clients to access the resources from a central computer known as a server using network services. Here, the clients make requests through a graphical user interface (GUI), and the server will give the desired output as soon as the instructions are matched. The process of MySQL environment is the same as the client-server model.<br><br>

				The core of the MySQL database is the MySQL Server. This server is available as a separate program and responsible for handling all the database instructions, statements, or commands. The working of MySQL database with MySQL Server are as follows:
	
				<ol style="list-style-type: 1;">
					<li>MySQL creates a database that allows you to build many tables to store and manipulate data and defining the relationship between each table.</li>
					<li>Clients make requests through the GUI screen or command prompt by using specific SQL expressions on MySQL.</li>
					<li>Finally, the server application will respond with the requested expressions and produce the desired result on the client-side.</li>
				</ol>
				A client can use any MySQL GUI. But, it is making sure that your GUI should be lighter and user-friendly to make your data management activities faster and easier. Some of the most widely used MySQL GUIs are MySQL Workbench, SequelPro, DBVisualizer, and the Navicat DB Admin Tool. Some GUIs are commercial, while some are free with limited functionality, and some are only compatible with MacOS. Thus, you can choose the GUI according to your needs.
