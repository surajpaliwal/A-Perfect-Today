		  		<div class="heading">What is Django?</div>
		  		<hr>
		  		<p>
		  		<strong>Django</strong> is a back-end python web development framework for building complex and scalable websites, and it might be the reason for python´s rise in popularity in the last years. Django uses the model-view-template (MVT) architecture, a pattern based on a set of best practices for organizing your code.<br><br>

				<strong>Model:</strong> The model is what binds your application to the database. Normally a model represents a table in the database and defines what and how your application accesses the database. It simplifies the tasks of creating, deleting or updating table entries.<br><br>

				<strong>View:</strong> The view is the user interface. It renders the templates and defines what you see and the behavior of it. It is composed of HTML, CSS, and javascript files.<br><br>

				<strong>Template:</strong> Is the desired static HTML structure file with special syntax. It describes the inserted content.<br><br>

