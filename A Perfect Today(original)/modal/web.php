					<div class="col-lg-6 text-right">
						<p class="center"><strong >WEB DEVELOPMENT BASICS</strong></p>
						<p>Web developers are responsible for designing and developing websites and website applications. Using a variety of programming languages and web technologies, web developers manage site functionality, implement application features, and integrate security measures.</p>
						<p class="center"><strong >WEB DEVELOPMENT IN-DEPTH</strong></p>
											
						<p>Web developers have expertise in object-oriented programming, multiplatform development, and the building of web applications. They blend creative vision with project management skills to plan, design and build websites and integrated applications. Because of the complex nature of websites, developers traditionally specialize in one of two tracks: front-end or back-end development.<br><br></p>	
