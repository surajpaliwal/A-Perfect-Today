				<p>
				Central to the success of a web developer are strong technical coding skills, and how to implement them within the context of Internet conventions and web applications. Beyond technical expertise, web developers must also have the right blend of soft skills in the workplace, including the ability to communicate clearly as well as a mindset geared to analytical problem-solving. Below is a list of common technical skills and competency areas that are important in web development.</p>
						
				<p class="center"><strong >HTML</strong></p>
				<p>Not a programming language, but the foundational element of any website. Includes the core elements of how a page should look.</p>

				<p class="center"><strong >Cascading Style Sheets (CSS)</strong></p>
				<p>CSS manages the way a page looks and serves as the guiding style instructions for a website—from fonts to color.</p>

				<p class="center"><strong >CSS Frameworks</strong></p>
				<p>CSS frameworks are used to structure and develop websites. They provide custom style guides that are used to help developers maintain industry best practices. Examples include Bootstrap and Foundation.</p>

				<p class="center"><strong >JavaScript</strong></p>
				<p>A programming language that controls how websites and dynamic web applications operate.</p>

				<p class="center"><strong >Responsive Web Design</strong></p>
				<p>Modern websites must adapt to mobile device use, requiring an understanding of responsive web design.</p>
						
				<p class="center"><strong >Version Control</strong></p>
				<p>When building a site, developers must manage the progress of their projects. Version control software, such as Git, allows developers to maintain and track changes to their code.</p>

				<p class="center"><strong >Browser Development Tools</strong></p>
				<p>Web developers need to understand how pages are rendered in a browser in order to identify bugs and improve performance.</p>

				<p class="center"><strong >Testing</strong></p>
				<p>When coding, bugs will be introduced into the system. Web developers should have familiarity with testing processes used to identify those bugs, and software frameworks such as Jasmine.</p>
				</p>
				
				<p class="center"><strong >Programming Languages</strong></p>
				<p>Depending on their professional specialty, web developers should be proficient in programming languages and frameworks such as Ruby on Rails, Python,java or PHP.</p>

