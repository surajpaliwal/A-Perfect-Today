				<div class="heading">WHAT DOES A WEB DEVELOPER Do?</div>
				<hr>
				<div class="row">
					<div class="col-lg-6 text-center">
						<img class=" img-border img-full" style="height:400px" src="web/web-dev/11.jpg" alt="">
