				<hr>
				<div class="heading">STEPS TO BECOMING A WEB DEVELOPER</div>
				<hr>
				<p>Web development is a flexible professional field, one that affords prospective students multiple ways to gain the training and experience necessary to begin their careers. Below is an overview of steps interested individuals can take to enter this career field.</p>

				<p class="center"><strong >1-CHOOSE A DEVELOPMENT SPECIALIZATION</strong></p>
				<div class="row">
					<div class="col-lg-6 text-center">
						<img class=" img-border img-full" style="height: 300px;" src="web/web-dev/5.jpg" alt="">	
					</div>
					<div class="col-lg-6 text-left">						
						<p>Each element of a website is linked to something else, and it’s a complex job to build a website successfully. Depending on their specific jobs, web developers will be proficient in using various software programs (e.g. JavaScript frameworks, version control, CSS), and will have not only the specialized knowledge that characterizes their roles, but also an understanding of the process as a whole. <br>Prior to starting out, it’s wise to get an understanding of the type of web development work that seems interesting—such as user interface design, mobile application development, or front-end development—to best set the stage for future education and training.</p>	
					</div>
					
				</div>
				
				<p class="center"><strong >2-GET AN EDUCATION</strong></p>				
				<div class="row">
					<div class="col-lg-6 text-center">
					 	<img class=" img-border img-full" src="web/web-dev/13.jpg" alt="">	
					</div>
					<div class="col-lg-6 text-left">					 		
					 	<p>No specific degree or educational path exists for careers in web development, and it’s possible to become a web developer with or without a formal education. While it’s certainly possible to learn coding languages independently, there is more to web development than coding. Individuals who can implement coding skills within a broader context may have an advantage, and that’s where a formal education can help prospective developers gain foundational knowledge of web development principles and practices, expose them to project management and situations that require problem-solving, and encourage critical thinking.<br>
					</div>
				</div>
				
				<p class="center"><strong >3-DEVELOP TECHNICAL SKILLS</strong></p>					
				<div class="row">
				  	<div class="col-lg-6 text-center">
					  	<img class=" img-border img-full" src="web/web-dev/6.jpg" alt="">	
					</div>
				  	<div class="col-lg-6 text-left">					  	
					  	<p>Despite the real advantages that come with an organized education, web development is still a field in which concrete skills can trump education when it comes to employment opportunities. Companies want employees who can produce, and how or where something was learned is secondary to simply being able to do it. Prospective web developers should have expertise in the three core programming components: HTML, CSS, and JavaScript. They should also learn about CSS and CSS frameworks such as Backbone, Bootstrap, and Foundation. Finally, to build applications, developers should be proficient in back-end languages such as Ruby, Java, PHP and tools such as Oracle and SQL Server.</p>
					</div>
					
