		  		<ul>
		  			<li><strong>Asynchronous and Event Driven</strong>
		  			 − All APIs of Node.js library are asynchronous, that is, non-blocking. It essentially means a Node.js based server never waits for an API to return data. The server moves to the next API after calling it and a notification mechanism of Events of Node.js helps the server to get a response from the previous API call.</li>

					<li><strong>Very Fast</strong> − Being built on Google Chrome's V8 JavaScript Engine, Node.js library is very fast in code execution.</li>

					<li><strong>Single Threaded but Highly Scalable</strong> − Node.js uses a single threaded model with event looping. Event mechanism helps the server to respond in a non-blocking way and makes the server highly scalable as opposed to traditional servers which create limited threads to handle requests. Node.js uses a single threaded program and the same program can provide service to a much larger number of requests than traditional servers like Apache HTTP Server.</li>

					<li><strong>No Buffering</strong> − Node.js applications never buffer any data. These applications simply output the data in chunks.</li>

					<li><strong>License</strong> − Node.js is released under the MIT license</li>
				</ul>
